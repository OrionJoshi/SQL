    --    job_id   |          avg
    -- ------------+------------------------
    --  AC_ACCOUNT |  8300.0000000000000000
    --  SA_MAN     |     12200.000000000000
    --  AD_PRES    |     24000.000000000000
    --  AC_MGR     | 12000.0000000000000000
