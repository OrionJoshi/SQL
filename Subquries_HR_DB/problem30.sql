-- Sample table: departments

--     +---------------+----------------------+------------+-------------+
-- | DEPARTMENT_ID | DEPARTMENT_NAME      | MANAGER_ID | LOCATION_ID |
-- +---------------+----------------------+------------+-------------+
-- |            10 | Administration       |        200 |        1700 |
-- |            20 | Marketing            |        201 |        1800 |
-- |            30 | Purchasing           |        114 |        1700 |
-- |            40 | Human Resources      |        203 |        2400 |


-- Solutions

-- Method-1

    SELECT first_name FROM employees 
    WHERE manager_id IN (SELECT manager_id FROM departments WHERE location_id 
    IN (SELECT location_id FROM locations WHERE country_id = 'US')) ORDER BY first_name;

-- Method-2

    SELECT DISTINCT first_name FROM employees e
    JOIN departments d ON e.manager_id = d.manager_id
    JOIN locations l ON l.location_id = d.location_id
    WHERE country_id = 'US'

